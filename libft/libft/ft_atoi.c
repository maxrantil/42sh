#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned long int	res;
	long int			minus;

	res = 0;
	minus = 1;
	while (ft_isspace(str))
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			minus = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = res * 10 + *str - 48;
		if (res > 9223372036854775807)
		{
			if (minus < 0)
				return (0);
			return (-1);
		}
		str++;
	}
	return (res * minus);
}
