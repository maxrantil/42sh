#include "libft.h"

int	ft_intlen(long nbr)
{
	int	c;

	c = 1;
	if (nbr < 0)
	{
		nbr *= -1;
		if (nbr == 0)
			c = 0;
		c++;
	}
	while (nbr > 9)
	{
		nbr = nbr / 10;
		c++;
	}
	return (c);
}
