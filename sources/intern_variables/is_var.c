#include "ft_42sh.h"

int	is_var(char *cmd)
{
	return ((ft_strchr(cmd, '=') && ft_isalpha(cmd[0]))
		|| (ft_strchr(cmd, '=') && !ft_isalpha(cmd[0]) && cmd[0] == '_'));
}
